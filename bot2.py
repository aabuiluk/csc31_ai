#!/usr/bin/env python3
"""Термінальний чат через OpenAI API, але тільки через requests."""

import os
import sys
from typing import Any

import requests
from dotenv import load_dotenv

# ---------------------------------------------------------------------------
# Модель (як у bot1.py)
# ---------------------------------------------------------------------------
# Можливі значення: ідентифікатор з каталогу OpenAI, напр. "gpt-4o-mini-2024-07-18",
# "gpt-4o-mini", "gpt-4o-2024-08-06", "gpt-3.5-turbo-16k" тощо.
# Вплив: якість, швидкість, ліміти контексту та ціна за токен (див. прайсинг OpenAI).
MODEL = "gpt-4o-mini-2024-07-18"
API_URL = "https://api.openai.com/v1/chat/completions"
EXIT_WORDS = {"exit", "quit", "q", "вихід", "вийти"}

# ---------------------------------------------------------------------------
# Параметри вибірки та «креативності»
# ---------------------------------------------------------------------------

# temperature — «температура» вибірки.
# Можливі значення: 0.0 … 2.0 (часто використовують 0–1). За замовчуванням у API — 1.
# Вплив: вище → більш випадкові/різноманітні відповіді; нижче → більш передбачувані
# та детерміністичніші (добре для фактів, коду, інструкцій). Не варто одночасно
# сильно піднімати і temperature, і top_p — краще змінювати щось одне.
TEMPERATURE = 0.7

# top_p — nucleus sampling: модель обирає токени з мінімальною множиною, сума ймовірностей яких ≥ top_p.
# Можливі значення: 0.0 … 1.0 (типово 1.0 = вимкнено обрізання за nucleus).
# Вплив: менше значення → вужчий «пул» токенів, відповіді стабільніші; 1.0 — повна кумулятивна
# маса до обрізання. Часто або налаштовують temperature, або top_p, а не обидва агресивно.
TOP_P = 1.0

# n — скільки варіантів завершення згенерувати за один запит.
# Можливі значення: ціле ≥ 1 (для чату зазвичай 1).
# Вплив: n>1 множить вартість (~n разів більше completion-токенів). У цьому скрипті
# показується лише перший варіант (choices[0]); інші можна додати в логіку за потреби.
N_COMPLETIONS = 1

# ---------------------------------------------------------------------------
# Обмеження довжини відповіді
# ---------------------------------------------------------------------------

# max_tokens — верхня межа токенів у відповіді асистента (completion).
# Можливі значення: ціле > 0 або None, щоб не передавати параметр (ліміт тоді задає модель/акаунт).
# Вплив: коротші відповіді дешевші; занадто мале значення обірве відповідь посеред речення.
# У новіших ендпойнтах інколи зустрічається max_completion_tokens — сенс той самий для «виходу» моделі.
MAX_TOKENS: int | None = 1024

# ---------------------------------------------------------------------------
# Штрафи за повторення (для довгих текстів корисніше, ніж для короткого чату)
# ---------------------------------------------------------------------------

# presence_penalty — штраф за вже згадані теми/токени (підштовхує до нових сюжетів).
# Можливі значення: -2.0 … 2.0; 0 = нейтрально.
# Вплив: >0 зменшує повторення ідей; <0 дозволяє сильніше «залипати» на тому самому.
PRESENCE_PENALTY = 0.0

# frequency_penalty — штраф за частоту конкретних токенів (слів).
# Можливі значення: -2.0 … 2.0; 0 = нейтрально.
# Вплив: >0 зменшує повторення тих самих слів/фраз; може зробити лексику різноманітнішою.
FREQUENCY_PENALTY = 0.0

# ---------------------------------------------------------------------------
# Зупинка генерації та відтворюваність
# ---------------------------------------------------------------------------

# stop — послідовності, при появі яких у виході генерація зупиняється.
# Можливі значення: None; один рядок; або до 4 рядків у списку (обмеження API).
# Вплив: зручно для структурованого виводу (наприклад, зупинитись перед маркером); у вільному чаті
# зазвичай None.
STOP_SEQUENCES: list[str] | None = None

# seed — насіння для псевдовипадковості (де підтримується).
# Можливі значення: ціле або None (не передавати).
# Вплив: при тих самих інших параметрах відповіді можуть бути стабільніші між запитами;
# гарантії біт-в-біт ідентичності немає для всіх моделей/навантажень.
SEED: int | None = None

# ---------------------------------------------------------------------------
# Зміщення ймовірностей окремих токенів (рідко потрібно в простому чаті)
# ---------------------------------------------------------------------------

# logit_bias — мапа token_id → зміщення логіту (-100 … 100 у типових прикладах; -100 фактично заборона).
# Можливі значення: порожній dict або dict після токенізації (потрібні id з токенайзера моделі).
# Вплив: примусово збільшити/зменшити шанс конкретних символів/слів; помилки легко «ламають» текст.
LOGIT_BIAS: dict[str, int] = {}

# ---------------------------------------------------------------------------
# Ідентифікатор кінцевого користувача (для моніторингу зловживань на стороні OpenAI)
# ---------------------------------------------------------------------------

# user — довільний рядок-ідентифікатор (наприклад, id у вашій БД), не PII якщо можна уникнути.
# Можливі значення: None (не передавати) або короткий стабільний id.
# Вплив: не змінює якість відповіді; допомагає провайдеру відстежувати аномалії по сесіях.
USER_STABLE_ID: str | None = None

# ---------------------------------------------------------------------------
# Логіти (для діагностики; трохи більше навантаження на відповідь)
# ---------------------------------------------------------------------------

# logprobs — чи повертати логарифми ймовірностей для обраних токенів.
# Можливі значення: True / False.
# Вплив: True дає додаткові поля в відповіді (корисно для аналізу впевненості моделі); на текст у чаті
# цей скрипт не виводить, лише assistant.message.content.
LOGPROBS = False

# top_logprobs — скільки альтернативних токенів на позицію показувати (коли logprobs=True).
# Можливі значення: 0 … 20 (залежить від версії API; 0 часто означає «без топу»).
# Вплив: більше число → більший обсяг метаданих у відповіді.
TOP_LOGPROBS = 0

# ---------------------------------------------------------------------------
# Параметри HTTP-запиту
# ---------------------------------------------------------------------------

# REQUEST_TIMEOUT_SEC — таймаут HTTP-запиту в секундах.
# Вплив: захищає від «зависань» мережі; якщо відповідь довга, можна збільшити значення.
REQUEST_TIMEOUT_SEC = 60


def _request_payload(messages: list[dict[str, str]]) -> dict[str, Any]:
    """Збирає JSON для /chat/completions; опускає None та порожні опції."""
    data: dict[str, Any] = {
        "model": MODEL,
        "messages": messages,
        "temperature": TEMPERATURE,
        "top_p": TOP_P,
        "n": N_COMPLETIONS,
        "presence_penalty": PRESENCE_PENALTY,
        "frequency_penalty": FREQUENCY_PENALTY,
    }
    if MAX_TOKENS is not None:
        data["max_tokens"] = MAX_TOKENS
    if STOP_SEQUENCES:
        data["stop"] = STOP_SEQUENCES
    if SEED is not None:
        data["seed"] = SEED
    if LOGIT_BIAS:
        data["logit_bias"] = LOGIT_BIAS
    if USER_STABLE_ID:
        data["user"] = USER_STABLE_ID
    if LOGPROBS:
        data["logprobs"] = True
        if TOP_LOGPROBS > 0:
            data["top_logprobs"] = TOP_LOGPROBS
    return data


def _call_chat_completions(
    api_key: str, messages: list[dict[str, str]]
) -> tuple[str | None, str | None]:
    """Повертає (reply, error). У випадку помилки reply=None."""
    headers = {
        "Authorization": f"Bearer {api_key}",
        "Content-Type": "application/json",
    }
    payload = _request_payload(messages)

    try:
        response = requests.post(
            API_URL,
            headers=headers,
            json=payload,
            timeout=REQUEST_TIMEOUT_SEC,
        )
    except requests.RequestException as e:
        return None, f"Помилка мережі/API: {e}"

    if response.status_code >= 400:
        try:
            err_json = response.json()
            err_message = err_json.get("error", {}).get("message", response.text)
        except ValueError:
            err_message = response.text
        return None, f"HTTP {response.status_code}: {err_message}"

    try:
        data = response.json()
    except ValueError:
        return None, "Відповідь API не є валідним JSON."

    try:
        reply = (data["choices"][0]["message"]["content"] or "").strip()
    except (KeyError, IndexError, TypeError):
        return None, "Несподіваний формат відповіді API."
    return reply, None


def main() -> None:
    load_dotenv()
    api_key = os.getenv("OPENAI_API_KEY", "").strip()
    if not api_key:
        print(
            "Помилка: не знайдено OPENAI_API_KEY.\n"
            "Скопіюйте .env.example у .env і додайте ключ API OpenAI.",
            file=sys.stderr,
        )
        sys.exit(1)

    print(
        f"Бот готовий (модель: {MODEL}). Питайте що завгодно; для виходу: exit / quit / вихід.\n"
    )
    # role у messages:
    # - "system": інструкції високого рівня (стиль, обмеження, правила відповіді).
    #   Зазвичай додається на старті історії і рідко змінюється.
    # - "user": повідомлення від користувача (запити/питання).
    # - "assistant": попередні відповіді моделі; потрібні для контексту діалогу.
    # - "tool": результат виклику інструмента (коли модель працює з tools/function calling).
    # - "function": застарілий формат ролі у старих інтеграціях.
    #
    # Важливо: порядок елементів у history має значення. API читає їх послідовно як історію
    # розмови, тому ми кожен новий user-запит і assistant-відповідь додаємо в кінець списку.
    # У цьому скрипті використовуємо базовий сценарій тільки з "user" і "assistant".
    history: list[dict[str, str]] = []

    while True:
        try:
            line = input("Ви> ").strip()
        except (EOFError, KeyboardInterrupt):
            print("\nДо побачення.")
            break

        if not line:
            continue
        if line.lower() in EXIT_WORDS:
            print("До побачення.")
            break

        history.append({"role": "user", "content": line})
        reply, err = _call_chat_completions(api_key, history)
        if err:
            history.pop()
            print(f"Помилка API: {err}", file=sys.stderr)
            continue

        history.append({"role": "assistant", "content": reply or ""})
        print(f"Бот> {reply}\n")


if __name__ == "__main__":
    main()
